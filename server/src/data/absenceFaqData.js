    '· 필수 서류: 진료비 납부 영수증',
    '',
    '■ 당일 진료',
    '· 제출기한: 진료 후 5일 이내 (주말 제외)',
    '· 필수 서류(택1): 진료확인서 또는 통원(진료)확인서',
    '· 필수 서류: 진료비 결제 영수증(작은 영수증)',
    '',
    '■ 병결 절차',
    '1. 서류를 자신의 학과사무실에 제출',
    '2. 2일 뒤 자신의 학과사무실에서 처리된 서류 수령',
