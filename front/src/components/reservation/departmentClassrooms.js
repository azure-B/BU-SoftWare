/** DB 학과명 → 강의실 데이터 학과명 */
export const DEPARTMENT_NAME_ALIASES = {
  첨단IT학부: '첨단 IT학부',
  '혁신교육플랫폼대학': '혁신융합학부',
};

function roomAmenities(capacity, equipment) {
  return [
    { icon: 'groups', text: `수용 인원: ${capacity}명` },
    { icon: 'videocam', text: `구비 시설: ${equipment}` },
  ];
}

export function normalizeDepartmentName(name) {
  if (!name) return '';
  return DEPARTMENT_NAME_ALIASES[name] ?? name;
}

export function filterDeptFacilities(facilities, targetDepartmentName) {
  const normalizedTarget = normalizeDepartmentName(targetDepartmentName);
  if (!normalizedTarget) return [];

  return facilities.filter(
    (facility) => normalizeDepartmentName(facility.departmentName) === normalizedTarget,
  );
}

export function buildDeptFacilities() {
  return DEPARTMENT_CLASSROOMS.flatMap((dept) =>
    dept.rooms.map((room, index) => ({
      id: room.id,
      departmentName: dept.name,
      title: room.title,
      available: room.available ?? index % 5 !== 4,
      description: `${dept.name} 소속 강의·실습 공간입니다. 학과 행사 및 수업 보조 목적으로 예약할 수 있습니다.`,
      amenities: roomAmenities(room.capacity, room.equipment),
      unavailableReason: room.unavailableReason ?? '학과 내부 일정으로 예약 불가',
      bookingMeta: {
        location: room.location,
        maxParticipants: room.capacity,
      },
    })),
  );
}

export function buildDeptFacilityBookingMeta() {
  const meta = {};
  buildDeptFacilities().forEach((facility) => {
    meta[facility.id] = {
      subtitle: facility.departmentName,
      location: facility.bookingMeta.location,
      maxParticipants: facility.bookingMeta.maxParticipants,
    };
  });
  return meta;
}

export function groupDeptFacilitiesByDepartment(facilities) {
  const groups = [];

  DEPARTMENT_CLASSROOMS.forEach((dept) => {
    const deptFacilities = facilities.filter((facility) => facility.departmentName === dept.name);
    if (deptFacilities.length > 0) {
      groups.push({ departmentName: dept.name, facilities: deptFacilities });
    }
  });

  return groups;
}
